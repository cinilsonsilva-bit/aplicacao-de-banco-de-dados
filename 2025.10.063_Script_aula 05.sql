CREATE TABLE FAMILIA (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL,
    PARENTESCO VARCHAR(50) NOT NULL,
    IDADE INT,
    CIDADE VARCHAR(50)
